package com.SeleniumwebdriverSession;

public class WhileLoop {

	public static void main(String[] args) {
		System.out.println("*************While*****************");
// While Loop
		int num = 1;
		while (num <= 10) {
			System.out.println(num);
			num++;
		}
		// Do while
		System.out.println("**********Do While*************");
		int num2 = 1;
		do {
			System.out.println(num2);
			num2++;

		} while (num2 <= 10);

	}

}
